export const handleColor = (type) => {
  if (type === "water") {
    return "#4492c4";
  } else if (type === "grass") {
    return "#9bcc50";
  } else if (type === "fire") {
    return "#fd7d24";
  } else if (type === "poison") {
    return "#b97fc9";
  } else if (type === "flying") {
    return "#c3b3f1";
  } else if (type === "normal") {
    return "#a4acaf";
  } else if (type === "bug") {
    return "#729f3f";
  } else if (type === "electric") {
    return "#eed535";
  } else if (type === "ice") {
    return "#50c4e7";
  } else if (type === "dark") {
    return "#707070";
  } else if (type === "ghost") {
    return "#7b62a2";
  } else if (type === "fighting") {
    return "#d56623";
  } else if (type === "ground") {
    return "#ab9842";
  } else if (type === "fairy") {
    return "#f9b8e9";
  } else if (type === "psychic") {
    return "#f366b9";
  } else if (type === "rock") {
    return "#a38b21";
  } else if (type === "steel") {
    return "#9db7b8";
  } else if (type === "dragon") {
    return "#f16e57";
  }
};
